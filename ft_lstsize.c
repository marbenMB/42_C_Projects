#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*now;
	int		i;

	i = 0;
	if (lst)
	{
		now = lst;
		while (now)
		{
			i++;
			now = now -> next;
		}
	}
	return (i);
}
