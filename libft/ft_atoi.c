int	ft_atoi(const char *str)
{
	char	*p;
	int		signe;
	int		k;

	p = (char *)str;
	signe = 1;
	k = 0;
	while (*p == '\t' || *p == '\n' || *p == '\v' || \
			*p == '\r' || *p == '\f' || *p == ' ')
		p++;
	if (*p == '-')
		signe = -signe;
	if (*p == '-' || *p == '+')
		p++;
	while (*p >= '0' && *p <= '9')
	{
		k = k * 10 +(*p - 48);
		p++;
	}
	return (k * signe);
}
